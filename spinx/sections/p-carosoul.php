<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Initialize the carousel with no auto-sliding
        const carousel = new bootstrap.Carousel(document.getElementById('techCarousel'), {
            interval: 5000,
            wrap: true // if true it will automatically restart from 1 slide on finish
        });

        // Handle number indicators click
        const indicators = document.querySelectorAll('.carousel-indicators button');
        indicators.forEach((indicator, index) => {
            indicator.addEventListener('click', () => {
                carousel.to(index);
            });
        });

        // Handle arrow navigation
        const prevButton = document.querySelector('.carousel-control-prev');
        const nextButton = document.querySelector('.carousel-control-next');

        prevButton.addEventListener('click', () => {
            carousel.prev();
        });

        nextButton.addEventListener('click', () => {
            carousel.next();
        });

        // Update indicators when slide changes
        document.getElementById('techCarousel').addEventListener('slide.bs.carousel', (event) => {
            const activeIndex = event.to;
            indicators.forEach((indicator, index) => {
                indicator.classList.toggle('active', index === activeIndex);
            });
        });
    });
</script>
